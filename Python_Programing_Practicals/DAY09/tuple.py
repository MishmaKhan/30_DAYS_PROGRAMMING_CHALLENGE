#  Write a Python program to create a tuple with numbers from 1 to 5, then use a loop to print each
#  element of the tuple.

# Create a tuple with numbers from 1 to 5
my_tuple = (1, 2, 3, 4, 5)

# Use a loop to print each element of the tuple
